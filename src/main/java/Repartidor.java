public class Repartidor {
    public int Cedula;
    public String Nombre;
    public int Edad;
    public int Num_Celular;
    public String Tipo_Licencia;
    public double Sueldo;

    public Repartidor(int cedula, String nombre, int edad, int num_Celular, String tipo_Licencia, double sueldo) {
        Cedula = cedula;
        Nombre = nombre;
        Edad = edad;
        Num_Celular = num_Celular;
        Tipo_Licencia = tipo_Licencia;
        Sueldo = sueldo;
    }
}
