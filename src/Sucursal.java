public class Sucursal {
    public int Codigo;
    public String Ciudad;
    public String Direccion;
    public int Cant_Empleados;

    public Sucursal(int codigo, String ciudad, String direccion, int cant_Empleados) {
        Codigo = codigo;
        Ciudad = ciudad;
        Direccion = direccion;
        Cant_Empleados = cant_Empleados;
    }
}
